#!/bin/bash
# =============================================================================
# XOne е®үе…ЁдёӢзәҝи„ҡжң¬ вҖ” иҮӘеҠЁеӨҮд»ҪеҗҺеҶҚ down
# жӣҝд»Ј docker compose down е‘Ҫд»Ө, йҳІжӯўзӣҙжҺҘ -v дёўеӨұж•°жҚ®
#
# з”Ёжі•:
#   ./scripts/safe-down.sh              вҶ’ docker compose down (дҝқз•ҷеҚ·)
#   ./scripts/safe-down.sh -v           вҶ’ е…ҲеӨҮд»Ҫ, еҶҚ down -v (й”ҖжҜҒеҚ·)
#   ./scripts/safe-down.sh --no-backup  вҶ’ и·іиҝҮеӨҮд»Ҫ, зӣҙжҺҘ down
# =============================================================================
set -euo pipefail

COMPOSE_FILE="docker/docker-compose.prod.yml"
DO_BACKUP=true

for arg in "$@"; do
    case "$arg" in
        --no-backup) DO_BACKUP=false ;;
    esac
done

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

log()  { echo -e "${GREEN}[$(date +%H:%M:%S)]${NC} $*"; }
warn() { echo -e "${YELLOW}[WARN]${NC} $*"; }
err()  { echo -e "${RED}[ERR]${NC} $*"; }

# в”Җв”Җв”Җ жЈҖжөӢжҳҜеҗҰеҢ…еҗ« -v еҸӮж•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if echo "$*" | grep -qE '\-v\b|--volumes' && $DO_BACKUP; then
    echo ""
    echo -e "${RED}в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—${NC}"
    echo -e "${RED}в•‘  вҡ  жЈҖжөӢеҲ° -v еҸӮж•°, е°Ҷй”ҖжҜҒжүҖжңүж•°жҚ®еҚ·!              в•‘${NC}"
    echo -e "${RED}в•‘  иҮӘеҠЁе…Ҳжү§иЎҢе…ЁйҮҸеӨҮд»Ҫ вҖҰ                             в•‘${NC}"
    echo -e "${RED}в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ${NC}"
    echo ""
    
    bash scripts/backup.sh
    log "еӨҮд»Ҫе®ҢжҲҗ, з»§з»ӯжү§иЎҢ down -v вҖҰ"
fi

# в”Җв”Җв”Җ жү§иЎҢ downв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
log "жү§иЎҢ: docker compose -f $COMPOSE_FILE down $*"
docker compose -f "$COMPOSE_FILE" down "$@"

log "е®үе…ЁдёӢзәҝе®ҢжҲҗ"
